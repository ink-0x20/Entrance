using UnityEngine;

using Entrance.Common;
using Entrance.Model;
using Entrance.Utils;
using Entrance.View;

using UniRx;

namespace Entrance.Prezenter
{
    public class TitlePrezenter : BaseScreenPrezenter
    {
        // **************************************************
        // View
        // **************************************************
        [SerializeField]
        private TitleView titleView = default;

        // **************************************************
        // Model
        // **************************************************
        private TitleModel titleModel = new TitleModel();

        // **************************************************
        // BGM
        // **************************************************
        [SerializeField]
        private AudioClip BGM = default;

        // ********************************************************************************
        // 初期化時に呼ばれます
        // ********************************************************************************
        public override void Initialize()
        {
            // ********************************************************************************
            // Viewの初期化（UIをアニメーション前の状態にする）
            // ********************************************************************************

            // ********************************************************************************
            // 追加で生成するべきUIパーツの生成と配置
            // ********************************************************************************
            // BGM再生
            AudioUtils.PlaySystemBGM(BGM, 1.0f);

            // ********************************************************************************
            // Bind処理（Modelの値の変更の監視設定）
            // ********************************************************************************
            // セッション取得
            titleModel.SessionCommon
                .Skip(1)
                .Subscribe((sessionCommon) =>
                {
                    // サーバーからセッションを取得
                    this.sessionCommon = sessionCommon;
                    // セッションロード後処理
                    this.sessionCommon.OnLoaded();
                    // 通信成功時のみ選曲画面へ遷移
                    if (!titleModel.IsCommunicationFailure.Value)
                    {
                        screenDataLinkage.Value = new ScreenDataLinkage(ScreenList.SelectMusicScreen, this.sessionCommon);
                    }
                })
                .AddTo(gameObject);
            // 通信失敗時エラーダイアログを表示
            titleModel.IsCommunicationFailure
                .Skip(1)
                .Subscribe((isCommunicationFailure) =>
                {
                    if (isCommunicationFailure)
                    {
                        // 通信エラーダイアログ表示
                        DialogHandler.ShowDialog(
                            "通信エラー",
                            "接続ができませんでした。\n電波状況の良いところで再度お試しください。",
                            "OK");
                    }
                })
                .AddTo(gameObject);

            // ********************************************************************************
            // SetEvents処理（Viewのイベントの監視設定）
            // ********************************************************************************
            titleView.sceneListener = () =>
            {
                if (this.sessionCommon == null)
                {
                    // サーバーへセッション検索リクエスト
                    titleModel.LoadSession();
                }
                else
                {
                    screenDataLinkage.Value = new ScreenDataLinkage(ScreenList.SelectMusicScreen, this.sessionCommon);
                }
            };

            // ********************************************************************************
            // 画面の描画に必要な追加通信
            // ********************************************************************************
        }

        // ********************************************************************************
        // OnScreenOepnの直前に呼ばれます
        // ********************************************************************************
