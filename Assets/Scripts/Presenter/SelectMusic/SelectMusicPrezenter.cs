using UnityEngine;

using Entrance.Common;
using Entrance.Model;
using Entrance.View;

using UniRx;

namespace Entrance.Prezenter
{
    public class SelectMusicPrezenter : BaseScreenPrezenter
    {
        // **************************************************
        // View
        // **************************************************
        [SerializeField]
        private SelectMusicView selectMusicView = default;

        // **************************************************
        // Model
        // **************************************************
        private SelectMusicModel selectMusicModel = new SelectMusicModel();

        // ********************************************************************************
        // 初期化時に呼ばれます
        // ********************************************************************************
        public override void Initialize()
        {
            // ********************************************************************************
            // Viewの初期化（UIをアニメーション前の状態にする）
            // ********************************************************************************
            selectMusicModel.Initialize(sessionCommon);
            selectMusicView.Initialize();

            // ********************************************************************************
            // 追加で生成するべきUIパーツの生成と配置
            // ********************************************************************************

            // ********************************************************************************
            // Bind処理（Modelの値の変更の監視設定）
            // ********************************************************************************
            // 楽曲楽曲リスト
            selectMusicModel.MusicList
                .Subscribe(selectMusicView.SetMusicList)
                .AddTo(gameObject);
            // 選択楽曲情報
            selectMusicModel.MusicInfo
                .Skip(1)
                .Subscribe(selectMusicView.SelectMusic)
                .AddTo(gameObject);

            // ********************************************************************************
            // SetEvents処理（Viewのイベントの監視設定）
            // ********************************************************************************
            // 選曲イベント
            selectMusicView.selectMusicListener1 = () => selectMusicModel.SelectMusic(1);
            selectMusicView.selectMusicListener2 = () => selectMusicModel.SelectMusic(2);
            selectMusicView.selectMusicListener3 = () => selectMusicModel.SelectMusic(3);
            selectMusicView.selectMusicListener4 = () => selectMusicModel.SelectMusic(4);
            selectMusicView.selectMusicListener5 = () => selectMusicModel.SelectMusic(5);
            // 楽曲移動イベント
            selectMusicView.leftPageListener = () => selectMusicModel.LeftPage();
            selectMusicView.rightPageListener = () => selectMusicModel.RightPage();
            // 楽曲決定イベント
            selectMusicView.decideListener = () =>
            {
                // 楽曲管理番号を保存
                sessionCommon.MusicNumber = selectMusicModel.MusicNumber.Value;
                // 楽曲名を保存
                sessionCommon.MusicName = selectMusicModel.MusicName;
                // 連携情報を保存
                selectMusicModel.LinkageSession(sessionCommon);
                // 次画面へ遷移
                screenDataLinkage.Value = new ScreenDataLinkage(ScreenList.SelectDifficultScreen, sessionCommon);
            };

            // ********************************************************************************
            // 画面の描画に必要な追加通信
            // ********************************************************************************
            // 過去選択楽曲をセット
            selectMusicModel.SetSelectedMusic(sessionCommon);
        }

        // ********************************************************************************
